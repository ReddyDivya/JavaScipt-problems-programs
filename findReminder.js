//Return the Remainder from Two Numbers

const fnReminder = (vDividend, vDivisor) => {
    return "The Reminder is "+ vDividend % vDivisor;
}

console.log(fnReminder(100, 10));//0
console.log(fnReminder(450, 7));//2
console.log(fnReminder(15, 4));//3

/*
Output
----------
The Reminder is 0
The Reminder is 2
The Reminder is 3
*/
